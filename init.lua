    },
  },
  formatting = {
    fields = { "kind", "abbr" },
    format = function(entry, vim_item)
      local kind = require("lspkind").cmp_format({ mode = "symbol", maxwidth = 50, symbol_map = cmp_icons })(entry, vim_item)
